/**
 * Excercise1.8
 * This is a class implemented a ClockTime and this class contains
 * 2 attribute: hour, minute
 */
public class ClockTime {
   private int hour;
   private int minute;
   /**
    * 
    * @param hour   The hour 
    * @param minute The minute 
    * Ex:
      + new ClockTime(18, 30);
      + new ClockTime(9, 45);
      + new ClockTime(22, 15);
    */
   public ClockTime(int hour, int minute) {
	this.hour = hour;
	this.minute = minute;
}
   
}
